#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	cnt;

	if (!s)
	{
		return (NULL);
	}
	cnt = 0;
	while (cnt < n)
	{
		*(char *)(s + cnt) = (char) c;
		cnt++;
	}
	return (s);
}
