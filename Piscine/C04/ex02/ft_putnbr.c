#include <unistd.h>

void	ft_putnbr(int nb)
{
	int				rem;
	long long int	n;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		rem = (n % 10) + 48;
		n = n / 10;
		ft_putnbr(n);
		write(1, &rem, 1);
	}
	else
	{
		n = n + 48;
		write(1, &n, 1);
	}
}
