int	ft_atoi(char *str)
{
	int	minus;
	int	res;

	minus = 1;
	res = 0;
	while (*str == ' ' || *str == '\n' || *str == '\t'
		|| *str == '\f' || *str == '\r' || *str == '\v')
	{
		str++;
	}
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
		{
			minus *= -1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9' && *str != '\0')
	{
		res = 10 * res + (*str - 48);
		str++;
	}
	return (minus * res);
}
