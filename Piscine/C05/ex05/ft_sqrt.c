int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 1)
	{
		return (1);
	}
	else
	{
		while (i <= nb / 2 && i < 46341)
		{
			if (nb == i * i)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
