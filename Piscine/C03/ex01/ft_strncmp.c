int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	idx;

	idx = 0;
	while (*s1 != '\0' && idx < n)
	{
		if (*s1 > *s2)
		{
			return (*s1 - *s2);
		}
		else if (*s1 < *s2)
		{
			return (*s1 - *s2);
		}
		s1++;
		s2++;
		idx++;
	}
	if (*s1 == '\0' && *s2 != '\0' && n >= 1)
	{
		return (*s1 - *s2);
	}
	else
	{
		return (0);
	}
}
